def alu(*inputs)
  instructions = DATA.readlines.map { |line| line.chomp.split }

  vars = {
    'w' => 0,
    'x' => 0,
    'y' => 0,
    'z' => 0
  }

  instructions.each do |i|
    case i
    in ['inp', var]
      vars[var] = inputs.shift
    in ['add', left, right]
      l = vars[left]
      r = vars[right] || right.to_i
      vars[left] = l + r
    in ['mul', left, right]
      l = vars[left]
      r = vars[right] || right.to_i
      vars[left] = l * r
    in ['div', left, right]
      l = vars[left]
      r = vars[right] || right.to_i
      vars[left] = l / r
    in ['mod', left, right]
      l = vars[left]
      r = vars[right] || right.to_i
      vars[left] = l % r
    in ['eql', left, right]
      l = vars[left]
      r = vars[right] || right.to_i
      l == r ? vars[left] = 1 : vars[left] = 0
    end
  end

  puts vars
  vars['z']
end

def decrement_model_number(number)
  digits = number.to_s.chars
  index = digits.size - 1

  loop do
    digit = digits[index].to_i

    if digit == 1
      index -= 1
      next
    else
      digit -= 1
      digits[index] = digit
      return digits.join.to_i
    end
  end
end

number = 99999999999999

loop do
  z = alu number
  if z.zero?
    puts number
    exit
  end

  number = decrement_model_number number
end

